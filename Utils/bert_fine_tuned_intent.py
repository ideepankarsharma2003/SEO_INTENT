def get_intent(keyword):
    print(f"feature_keyword: {keyword}")
    raw_result= my_reloaded_model(tf.constant(pd.Series(keyword).values))
    raw_result_softmax= tf.keras.activations.softmax(raw_result)
    max_index= np.argmax(raw_result_softmax[0])
    print(f"raw result: {raw_result}")
    print(f"softmax: {raw_result_softmax}")
    
    print(f"maximum index: {max_index}, intent: {intent[max_index]}")
    print()
    raw_result_softmax= raw_result_softmax.numpy().tolist()
    
    d= {}
    for i in range(5):
        d[intent[i]]= str(raw_result_softmax[0][i])
        
    d["dominant_intent"]= intent[max_index]
    print(d)
    return  d

